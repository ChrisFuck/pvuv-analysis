type resource struct {
	url      string
	target   string
	start    int
	end      int
}

func ruleResource () []resource {
	var res []resource
	r1 := resource {
		url: "http://localhost:8888/",
		target:"",
		start:0,
		end:0,
	}
	r2 := resource{
		url:"http://localhost:8888/list/{$id}.html",
		target:"{$id}",
		start:1,
		end:21,
	}
	r3 := resource{
		url:"http://localhost:8888/movie/{$id}.html",
		target:"{$id}",
		start:1,
		end:12924,
	}
	res = append(append(append(res,r1),r2),r3)
	return res
}

func buildUrl(res []resource) []string {
	var list []string

	for _,r := range res {
		if len(r.target) == 0 {
			list = append(list,r.url)
		} else {
			for i := r.start; i<= r.end; i++ {
				urlStr := strings.Replace(r.url, r.target, strconv.Itoa(i),-1)
				list = append(list,urlStr)
			}
		}
	}

	return list
}

func makeLog(current , refer, ua string) string {
	u := url.Values{}
	u.Set("time","1")
	u.Set("url",current)
	u.Set("refer",refer)
	u.Set("ua",ua)
	paramsStr := u.Encode()

	logTemplate := "127.0.0.1 - - [28/Nov/2018:14:43:31 +0800] \"OPTIONS /dig?{$paramsStr} HTTP/1.1\" 200 43\"-\" \"{$ua}\" \"-\""
	log := strings.Replace(logTemplate,"{$paramsStr}", paramsStr,-1)
	log = strings.Replace(log,"{$ua}",ua,-1)
	return log
}

func randInt(min,max int) int {
	r := rand.New(rand.NewSource(time.Now().UnixNano()))
	if min > max {
		return max
	}
	return r.Intn(max-min) + min
}

